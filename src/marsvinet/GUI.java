    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        String usernameInput = jTextFieldUserName.getText();
        String passwordInput = jTextFieldPassWord.getText();

        if (logic.login(usernameInput, passwordInput)) {
            jTabbedPane1.add("New Member", jPanelNewMember);
            jTabbedPane1.add("Hidden", jPanelHidden);
            jPanelMembers.remove(jTextFieldUserName);
            jPanelMembers.remove(jTextFieldPassWord);
            jPanelMembers.remove(jButtonLogin);
            jPanelMembers.add(jButtonLogout);
        } else {
            JOptionPane.showMessageDialog(this, "You shall not pass, fool..");
        }


    }//GEN-LAST:event_jButtonLoginActionPerformed

